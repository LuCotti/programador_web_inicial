<h2>Historia</h2>
<a href="#header">Volver al inicio</a>
<p>Linkin Park es una banda estadounidense de rock alternativo procedente de Agoura Hills, California formada en 1996. Integrada por
Mike Shinoda, Dave Farrell, Joe Hahn, Brad Delson, Rob Bourdon y Chester Bennington, este último como voz principal. La banda
comenzó con sus primeros trabajos musicales de manera independiente, en la que grabaron su primer material, llamado Xero; sin
embargo, no tuvieron éxito en la búsqueda de un sello discográfico, ya que nadie mostraba interés por su trabajo. No fue hasta
1999, que con el apoyo de Jeff Blue, quien ejercía como vicepresidente de Warner Records, lograron firmar su primer contrato. El
nombre proviene de un juego de palabras que hace referencia al Lincoln Park en Santa Mónica.</p>
<p>En octubre de 2000, el grupo lanzó su álbum debut, titulado Hybrid Theory, en el que se encuentran sencillos populares como
«Crawling», «Papercut», «In the End» y «One Step Closer»; los últimos dos obtuvieron certificación de disco de oro en Australia.
Hybrid Theory que alcanzó el puesto número dos en el Billboard 200, fue el séptimo álbum más vendido de la década del 2000,13 y
certificado con disco de diamante en los Estados Unidos y múltiple disco de platino en Europa. El cuarto sencillo del álbum,
«In the End», alcanzó su mayor posición al situarse en el segundo puesto de la lista Billboard Hot 100.</p>
<p>En marzo de 2003, salió al mercado su segundo álbum de estudio, Meteora. Solo en las primeras semanas logró ventas de 810 000
unidades, el álbum entró en el Billboard 200 en el puesto número uno, y se convirtió en el tercer disco más vendido del año. De
este disco surgieron canciones como «Somewhere I Belong» y «Numb»; esta última recibió un disco de oro en los Estados Unidos y
Australia. Ese mismo año, MTV2 nombró a Linkin Park como la sexta «mejor banda en la era del vídeo musical» y la tercera «mejor
banda del nuevo milenio», detrás de Oasis y Coldplay. Asimismo, Billboard ubicó al grupo en el puesto diecinueve de los mejores
artistas de la década. En noviembre de ese mismo año, la banda lanzó su primer álbum en directo Live in Texas, que grabaron en el
Reliant Stadium, en Houston, Texas. El disco obtuvo múltiples certificaciones de oro en Alemania, Nueva Zelanda, Reino Unido y
Austria. En 2008 lanzaron otro álbum en vivo, Road to Revolution: Live at Milton Keynes.</p>
<p>Después de Meteora, la banda pospuso por un par de años su siguiente material discográfico, el cual aprovecharon algunos
integrantes en hacer proyectos alternos. La banda también lanzó dos álbumes de remezcla certificados entre ese lapso, Reanimation
(2002) y Collision Course (2004), este último con colaboración del rapero Jay-Z en la canción «Numb/Encore». Dicho sencillo recibió
certificación en los premios Grammy de 2006.</p>
<p>El grupo es conocido por su estilo musical característico que mezcla de rock, rap y sonidos electrónicos, si bien desde sus inicios
se le consideró como nu metal, hasta lo que fue su segundo álbum Meteora, en ese entonces decidieron por este y el rap metal; no
obstante, en su tercer disco Minutes to Midnight exploraron de nuevo otros géneros, y eligieron un estilo más «suave», orientado al
rock alternativo. Posteriormente con su disco A Thousand Suns registraron un estilo mucho más experimental, dicha producción se
convirtió en el cuarto álbum consecutivo de la banda en alcanzar el primer puesto en la lista de popularidad en su primera semana de
ventas. La banda ha vendido 100 millones de discos y obtuvo dos premios Grammy.</p>
